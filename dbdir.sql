CREATE DATABASE dbdir;
USE dbdir

CREATE TABLE category(
c_id int NOT NULL PRIMARY KEY,
c_name varchar(100)
);

CREATE TABLE sub_categ_one(
sbo_id int NOT NULL PRIMARY KEY,
sbo_name varchar(100),
c_id int,
FOREIGN KEY(c_id) REFERENCES category(c_id) on DELETE cascade on UPDATE cascade
);

CREATE TABLE sub_categ_two(
sbt_id int NOT NULL PRIMARY KEY,
sbt_name varchar(100),
sbo_id int,
FOREIGN KEY(sbo_id) REFERENCES sub_categ_one(sbo_id) on DELETE cascade on UPDATE cascade
);

CREATE TABLE usr(
usr_id int NOT NULL PRIMARY KEY,
usr_name varchar(100) NOT NULL,
usr_email varchar(400) UNIQUE NOT NULL,
usr_passwd varchar(10) NOT NULL
);

CREATE TABLE image(
img_id int NOT NULL PRIMARY KEY

);

CREATE TABLE location(
loc_id int NOT NULL PRIMARY KEY,
loc_name varchar(100)
);

CREATE TABLE ad(
ad_id int NOT NULL PRIMARY KEY,
ad_name varchar(400),
ad_title varchar(500),
ad_keywd varchar(100),
ad_date date,
ad_pnum int(15),
c_id int,
FOREIGN KEY(c_id) REFERENCES category(c_id) on DELETE cascade on UPDATE cascade,
sbo_id int,
FOREIGN KEY(sbo_id) REFERENCES sub_categ_one(sbo_id) on DELETE cascade on UPDATE cascade,
sbt_id int,
FOREIGN KEY(sbt_id) REFERENCES sub_categ_two(sbt_id) on DELETE cascade on UPDATE cascade,
usr_id int,
FOREIGN KEY(usr_id) REFERENCES usr(usr_id) on DELETE cascade on UPDATE cascade,
loc_id int,
FOREIGN KEY(loc_id) REFERENCES location(loc_id) on DELETE cascade on UPDATE cascade,
img_id int,
FOREIGN KEY(img_id) REFERENCES image(img_id) on DELETE cascade on UPDATE cascade
);


